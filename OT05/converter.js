// Função para converter polegadas em centímetros
const converterPolegadasParaCentimetros = (polegadas) => polegadas * 2.54;

// Exemplo de uso da função
const polegadas = 10; // Você pode alterar este valor para testar diferentes comprimentos em polegadas
const centimetros = converterPolegadasParaCentimetros(polegadas);
console.log(`${polegadas} polegadas são equivalentes a ${centimetros} centímetros.`);
