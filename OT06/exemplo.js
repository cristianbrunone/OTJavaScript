let pessoa = {
    nome: "Yrina",
    idade: 28,
    profissao: "Engenheira"
};

// Usar Object.keys() para listar todas as chaves do objeto
let chaves = Object.keys(pessoa);
console.log("Chaves do objeto:", chaves);

// Usar Object.values() para listar todos os valores do objeto
let valores = Object.values(pessoa);
console.log("Valores do objeto:", valores);