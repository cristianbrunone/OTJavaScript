// 1. Criar um objeto carro com as propriedades marca, modelo e ano
let carro = {
    marca: "Toyota",
    modelo: "Corolla",
    ano: 2020
};

// Acessar e imprimir a propriedade modelo do objeto carro
console.log("Modelo do carro:", carro.modelo);

// 2. Adicionar a propriedade cor e modificar o ano do carro
carro.cor = "Azul"; // Adicionar a propriedade cor
carro.ano = 2021;   // Modificar a propriedade ano

// Imprimir o objeto carro modificado
console.log("Carro modificado:", carro);